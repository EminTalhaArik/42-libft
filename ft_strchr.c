#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	s_length;
	size_t	i;

	s_length = ft_strlen(s);
	i = 0;
	while (i < s_length)
	{
		if (s[i] == (char)c)
		{
			return ((char *)&s[i]);
		}
		i++;
	}
	if ((char)c == '\0')
	{
		return ((char *)&s[i]);
	}
	return (NULL);
}
